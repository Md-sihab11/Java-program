class ASCII{
	
	public static void main(String[] args)
	{
		//ascii value we will try here
		char ch='a';
		int asc= (int)ch;
		System.out.println(asc);
		
		//increment and decrement 
		
		int x=9;
		System.out.printf("%d\n",x);
		System.out.printf("%d\n",x++);
		System.out.printf("%d\n",++x);
		System.out.printf("%d\n",x++);
		System.out.printf("%d\n",--x);
		
		
	}
}